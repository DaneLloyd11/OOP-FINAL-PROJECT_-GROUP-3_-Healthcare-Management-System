    private void iDrugCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_iDrugCodeKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            String  drugCode = iDrugCode.getText();
            
            try {
                pst = con.prepareStatement("SELECT * FROM item WHERE `Item ID` = ? ");
                pst.setString(1, drugCode);
                rs = pst.executeQuery();
                
                if(rs.next() == false)
                {
                    JOptionPane.showMessageDialog(this, "Drug Not Found!");
                }
                else
                {
                    String drugName = rs.getString("Item Name");
                    iDrugName.setText(drugName.trim());
                    iQuantity.requestFocus();
                }
                
                
            } catch (SQLException ex) {
                Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_iDrugCodeKeyPressed

