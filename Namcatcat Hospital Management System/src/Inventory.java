    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        
        String drugCode = iDrugCode.getText();
        
        try {
            pst = con.prepareStatement("SELECT * FROM item WHERE `Item ID` = ? ");
            pst.setString(1, drugCode);
            rs = pst.executeQuery();
            
            while(rs.next())
            {
                int currentQuantity;
                int sellPrice;
                int Quantity;
                
                currentQuantity = rs.getInt("Quantity");
                sellPrice = rs.getInt("Sell Price");
                
                Quantity = Integer.parseInt(iQuantity.getValue().toString());
                
                int total = sellPrice * Quantity;
                
                
                
                if(Quantity >= currentQuantity)
                {
                    JOptionPane.showMessageDialog(this, "Avaliabel Item:" + currentQuantity);
                    JOptionPane.showMessageDialog(this, "The Chosen Item is Unavaliable");
                }
                else
                {
                    DefaultTableModel DF = (DefaultTableModel)inventoryTable.getModel();
                    DF.addRow(new Object[]
                    {
                        prescriptionIDLabel.getText(),
                        iDrugCode.getText(),
                        iDrugName.getText(),
                        iQuantity.getValue().toString(),
                        sellPrice,
                        total
                    });
                    
                    int sum = 0;
                    
                    for(int i = 0; i < inventoryTable.getRowCount(); i++)
                    {
                        sum = sum + Integer.parseInt(inventoryTable.getValueAt(i,5).toString());
                    }
                    
                    iTotalCost.setText(Integer.toString(sum));
                    iDrugCode.setText("");
                    iDrugName.setText("");
                    iQuantity.setValue(0);
                    iDrugCode.requestFocus();
                    
                }

                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_AddButtonActionPerformed

