    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        
        String patientNumber = patientNumberLabel.getText();
        String patientName = prPatientname.getText();
        String phoneNumber = prPhonenumber.getText();
        String address = prAddress.getText();
        
        if (!phoneNumber.matches("\\d{11}"))
         {
            JOptionPane.showMessageDialog(this, "Please enter a 11-digit numeric phone number", "Phone Number Error", JOptionPane.WARNING_MESSAGE);
            return;
         }
        
        try {
            pst = con.prepareStatement("INSERT INTO patient (`Patient Number`, `Patient Name`, `Phone Number`, `Address`) VALUES (?, ?, ?, ?)");
            pst.setString(1, patientNumber);
            pst.setString(2, patientName);
            pst.setString(3, phoneNumber);
            pst.setString(4, address);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Data Has Been inserted!");
           
            AutoID();
            prPatientname.setText("");
            prPhonenumber.setText("");
            prAddress.setText("");
            prPatientname.requestFocus();
            
            patient_Table();
            
        } catch (SQLException ex) {
            Logger.getLogger(PatientForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Please enter a 11-digit phone number", "Phone Number Error", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_AddButtonActionPerformed

