    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        
        String doctorNumber = doctorNumberLabel.getText();
        String doctorName = drDoctorname.getText();
        String specialization = drSpecialization.getText();
        String qualification = drQualification.getText();
        String channelFee = drChannelfee.getText();
        String phoneNumber = drPhonenumber.getText();
        String roomNumber = drRoomnumber.getValue().toString();
        
        if (!phoneNumber.matches("\\d{11}"))
         {
            JOptionPane.showMessageDialog(this, "Please enter a 11-digit numeric phone number", "Phone Number Error", JOptionPane.WARNING_MESSAGE);
            return;
         }
        
       
        try {
            pst = con.prepareStatement("INSERT INTO doctor (`Doctor Number`, `Doctor Name`, `Specialization`, `Qualification`, `Channel Fee`, `Phone Number`, `Room Number`, `Log In`) VALUES (?, ?, ?, ?, ?, ?, ?, ?)");
            pst.setString(1, doctorNumber);
            pst.setString(2, doctorName);
            pst.setString(3, specialization);
            pst.setString(4, qualification);
            pst.setString(5, channelFee);
            pst.setString(6, phoneNumber);
            pst.setString(7, roomNumber);
            pst.setInt(8, newID);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Data Has Been inserted!");
           
            AutoID();
            drDoctorname.setText("");
            drSpecialization.setText("");
            drQualification.setText("");
            drChannelfee.setText("");
            drPhonenumber.setText("");
            drRoomnumber.setValue(0);

            drDoctorname.requestFocus();
            
            doctor_Table();
            
        } catch (SQLException ex) {
            Logger.getLogger(PatientForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Please enter a 11-digit phone number", "Phone Number Error", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_AddButtonActionPerformed

