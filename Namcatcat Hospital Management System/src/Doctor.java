    public void AutoID() {
    try {
        Statement s = con.createStatement();
        rs = s.executeQuery("SELECT MAX(`Doctor Number`) FROM doctor");
        rs.next();
        String maxDoctorNumber = rs.getString(1);

        if (maxDoctorNumber == null) {
            doctorNumberLabel.setText("DS001");
        } else {
            long ID = Long.parseLong(maxDoctorNumber.substring(2)) + 1;
            doctorNumberLabel.setText("DS" + String.format("%03d", ID));
        }
    } catch (SQLException ex) {
        Logger.getLogger(PatientForm.class.getName()).log(Level.SEVERE, null, ex);
    } finally {
        try {
            if (rs != null) {
                rs.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(PatientForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
}
    public void doctor_Table()
    {
        try {
            pst = con.prepareStatement("SELECT * FROM doctor WHERE `Log ID` = ?");
            pst.setInt(1, newID);
            rs = pst.executeQuery();
            ResultSetMetaData Rsm = rs.getMetaData();
            int c;
            c = Rsm.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)drList.getModel();
            df.setRowCount(0);
            
            while(rs.next())
                    {
                        Vector v2 = new Vector();
                        
                        for(int i = 1; i <= c; i++)
                        {
                        v2.add(rs.getString("Doctor Number"));
                        v2.add(rs.getString("Doctor Name"));
                        v2.add(rs.getString("Specialization"));
                        v2.add(rs.getString("Qualification"));
                        v2.add(rs.getString("Channel Fee"));
                        v2.add(rs.getString("Phone Number"));
                        v2.add(rs.getString("Room Number"));
                        }
                        
                        df.addRow(v2);
                    }
            
        } catch (SQLException ex) {
            Logger.getLogger(PatientForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    

